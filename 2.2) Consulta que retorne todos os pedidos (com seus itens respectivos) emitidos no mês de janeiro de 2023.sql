SELECT 
    C5.C5_NUM,
    C5.C5_EMISSAO,
    C5.C5_CODCLI,
    C5.C5_CODVEN,
    C6.C6_ITEM,
    C6.C6_CODPROD,
    C6.C6_QTD,
    C6.C6_PRUNIT
FROM 
    C5_Cadastro_de_Pedidos C5
JOIN 
    C6_Itens_do_Pedido C6 ON C5.C5_NUM = C6.C6_NUM
WHERE 
    C5.C5_EMISSAO BETWEEN '2023-01-01' AND '2023-01-31';
